package net.hycube.messaging.messages;

public class MessageByteConversionRuntimeException extends RuntimeException {

	
	/**
	 * 
	 */
	private static final long serialVersionUID = 6671994534079307859L;

	public MessageByteConversionRuntimeException() {
		super();
	}

	public MessageByteConversionRuntimeException(String msg, Throwable e) {
		super(msg, e);
	}

	public MessageByteConversionRuntimeException(String msg) {
		super(msg);
	}

	public MessageByteConversionRuntimeException(Throwable e) {
		super(e);
	}

	
	
}
